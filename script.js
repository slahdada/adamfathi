function addMessage(text, isUser = false) {
    const msg = document.createElement('div');
    msg.className = isUser ? 'glass' : 'glass';
    msg.style.padding = '10px';
    msg.style.fontSize = '0.9rem';
    msg.style.borderRadius = 'var(--border-radius)';

    if (isUser) {
        msg.style.background = 'var(--accent)';
        msg.style.color = 'white';
        msg.style.alignSelf = 'flex-end';
        msg.style.borderBottomRightRadius = '0';
    } else {
        msg.style.alignSelf = 'flex-start';
        msg.style.borderBottomLeftRadius = '0';
    }

    msg.innerText = text;
    aiChatBox.appendChild(msg);
    aiChatBox.scrollTop = aiChatBox.scrollHeight;
}

aiSend.addEventListener('click', processAiInput);
aiInput.addEventListener('keypress', (e) => {
    if (e.key === 'Enter') processAiInput();
});

// Gemini AI Configuration (Local Mock)
async function processAiInput() {
    const val = aiInput.value.trim().toLowerCase();
    if (!val) return;

    addMessage(aiInput.value.trim(), true);
    aiInput.value = '';

    // Afficher un indicateur de chargement
    const loadingMsg = document.createElement('div');
    loadingMsg.innerText = "...";
    loadingMsg.style.padding = "10px";
    aiChatBox.appendChild(loadingMsg);

    // Simulation d'une réflexion de l'IA (délai de 1 à 2 secondes)
    const delay = Math.floor(Math.random() * 1000) + 1000;
    
    setTimeout(() => {
        aiChatBox.removeChild(loadingMsg);
        let responseKey = 'default';

        // Logique de mots-clés "Mock"
        if (val.includes("prix") || val.includes("tarif") || val.includes("cost") || val.includes("سعر")) {
            responseKey = 'price';
        } else if (val.includes("logiciel") || val.includes("software") || val.includes("3d") || val.includes("bim") || val.includes("برنامج")) {
            responseKey = 'software';
        } else if (val.includes("temps") || val.includes("délai") || val.includes("time") || val.includes("وقت")) {
            responseKey = 'delay';
        } else if (val.includes("contact") || val.includes("numéro") || val.includes("whatsapp") || val.includes("رقم")) {
            responseKey = 'default'; // par défaut donne le contact dans le texte original
        }

        const aiText = aiResponses[currentLang][responseKey];
        addMessage(aiText);
    }, delay);
}

// Scroll Animation Observer
const observerOptions = {
    root: null,
    rootMargin: '0px',
    threshold: 0.15
};

const scrollObserver = new IntersectionObserver((entries, observer) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('show');
            // Optionnel : ne pas rejouer l'animation en remontant
            observer.unobserve(entry.target); 
        }
    });
}, observerOptions);

// Au chargement du DOM
document.addEventListener('DOMContentLoaded', () => {
    const animatedElements = document.querySelectorAll('.animate-on-scroll');
    animatedElements.forEach((el) => scrollObserver.observe(el));
});
