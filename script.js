// Language Logic
const langBtn = document.getElementById('lang-btn');
const currentLangSpan = document.getElementById('current-lang');

langBtn.addEventListener('click', () => {
    const langs = ['fr', 'en', 'ar'];
    let idx = langs.indexOf(currentLang);
    currentLang = langs[(idx + 1) % langs.length];

    updateLanguage();
});

function updateLanguage() {
    currentLangSpan.innerText = currentLang.toUpperCase();
    document.documentElement.setAttribute('lang', currentLang);
    document.documentElement.setAttribute('dir', currentLang === 'ar' ? 'rtl' : 'ltr');

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[currentLang][key]) {
            el.innerHTML = translations[currentLang][key];
        }
    });

    // Update Placeholder logic if needed
    const aiInput = document.getElementById('ai-input');
    if (currentLang === 'ar') {
        aiInput.placeholder = "اطرح سؤالاً تقنياً...";
    } else if (currentLang === 'en') {
        aiInput.placeholder = "Ask a technical question...";
    } else {
        aiInput.placeholder = "Posez une question technique...";
    }
}

